// OurMath.js

const divide = require('./divide');
const multiply = require('./multiply');

class OurMath
{
    static divide(a, b)
    {
        return divide(a, b);
    }

    static multiply(a, b)
    {
        return multiply(a, b);
    }
}

module.exports = OurMath;

